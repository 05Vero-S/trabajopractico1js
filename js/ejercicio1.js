//Variables y operaciones matemáticas

//Declaro las 2 variables
const numero1 = parseInt(prompt("Ingrese un número: "));
console.log(numero1);
const numero2 = parseInt(prompt("Ingrese otro número: "));
console.log(numero2);

//Suma
console.log("Resultado suma: " + (numero1 + numero2));

//Resta
console.log("Resultado resta: " + (numero1 - numero2));

//Multiplicacion
console.log("Resultado multiplicación: " + (numero1*numero2));

//Division
console.log("Resultado división: " + (numero1/numero2));